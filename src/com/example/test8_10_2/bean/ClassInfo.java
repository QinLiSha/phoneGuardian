package com.example.test8_10_2.bean;

public class ClassInfo {

	private String name;
	private int idx;

	public String getName() {
		return name;
	}

	public ClassInfo(String name, int idx) {
		super();
		this.name = name;
		this.idx = idx;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}
}
